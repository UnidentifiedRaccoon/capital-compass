    'üìä –†–µ–∑—É–ª—å—Ç–∞—Ç—ã:',
    '–¢—Ä–µ–±—É–µ–º—ã–π –≤–∑–Ω–æ—Å:',
    '–ü—Ä–æ–≥–Ω–æ–∑ –∫–∞–ø–∏—Ç–∞–ª–∞',
    '–ï–∂–µ–º–µ—Å—è—á–Ω–∞—è –≤—ã–ø–ª–∞—Ç–∞',
    '–†–∞–∑–±–∏–≤–∫–∞ –ø—Ä–∏—Ç–æ–∫–∞:',
